        <div class="eight wide column"><h2>Piano</h2>
            <p>O som é produzido por peças feitas de madeira pintadas por um material macio e designados martelos, sendo ativados através de um teclado, tocam nas cordas esticadas e presas numa 
            estrutura rígida de madeira ou metal. As cordas vibram e produzem o som. Como instrumento de cordas percutidas por mecanismo ativado por um teclado.
            Teve a sua primeira referência publicada em 1711, no "Giornale dei Litterati d'Italia" por motivo da sua apresentação em Florença pelo seu inventor Bartolomeo Cristofori.A partir 
            desse momento sucede-se uma série de aperfeiçoamentos até chegar ao piano atual. O piano é amplamente utilizado na música ocidental, no jazz, para a performance solo e para acompanhamento.
            É também muito popular como um auxílio para compor. Embora não seja portátil e tenha um alto preço, o piano é um instrumento versátil, uma das características que o tornou um dos
            instrumentos musicais mais conhecidos pelo mundo.</p>
