        <h3>TEATRO COM FANTOCHES</h3>
        <p>O teatro de fantoches, de bonecos ou de marionetes é a expressão teatral que caracteriza as encenações
        realizadas, respectivamente, com fantoches, marionetes ou bonecos. É ainda mais autêntico aquele no qual seus
        elementos são estritamente criados para determinada representação. Este instrumento teatral conferia aos seres
        que os utilizavam poderes mágicos, caracterizando-os como personas intermediárias entre os povos primitivos e
        seus deuses. As pessoas conferiam tal sacralidade ao fantoche que ele realmente parecia sustentá-las
        espiritualmente. Ao se tornar portador do fantoche, o personagem adquiria poderes que o convertia em um
        profeta, um ser sagrado, um exorcista. Portanto, somente um iniciado nos conhecimentos sacros poderia usar suas
        mãos para dar vida ao fantoche, em uma cerimônia especialmente preparada para essa encenação.</p>
