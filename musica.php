                <p class="font-big">O violão é um instrumento de cordas, com uma caixa geralmente feita de madeira, que gera uma acústica facilitando a propagação do som. Em alguns países de língua espanhola é conhecido como guitarra. A utilização do violão é umas das mais diversificadas, podendo ser utilizado tanto na música instrumental (orquestras), quanto em acompanhamento da voz (canções solo).</p>
