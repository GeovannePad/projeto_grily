        <div class="eight wide column"><h2>Violino</h2>
            <p>Uns dos intrumentos mais tocados e um dos primeiros a ser classificados como um instrumento de cordas friccionadas(vibração entre cordas), era chamado de rabeca antes de possuir o nome violino.
            É o menor e mais agudo dos instrumentos, possuindo 4 cordas com afinações de aguda à grave, toca-se com um arco de madeira, dedilhando ou por meio da percussão possui maior utilização nas
            orquestras, nas músicas de concerto, mas pode ser usado nos outros estilos musicais e o líder dos violinos nas orquestras é chamado de spalla, e em seguida o maestro, líder da orquestra.</p>
