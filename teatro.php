        <h3>TEATRO DE SOMBRAS</h3>
        <p>O teatro de sombras é uma arte muito antiga de contar histórias e de entretenimento que usa bonecos de
            sombra.
            O teatro de sombras é uma arte muito antiga, originária da China, de onde se espalhou para o mundo.
            Consiste na
            manipulação de um boneco de varas, entre uma luz e uma tela, o que faz com que o espectador, sentado diante
            da
            tela, veja apenas a sombra do boneco. Existe uma lenda chinesa a respeito da origem do teatro de sombras.
            No
            ano 121, o imperador Wu Ti, da dinastia Han, desesperado com a morte de sua bailarina favorita, teria
            ordenado
            ao mago da corte que a trouxesse de volta do "Reino das Sombras", caso contrário ele seria decapitado. O
            mago
            usou a sua imaginação e, com uma pele de peixe macia e transparente, confeccionou a silhueta de uma
            bailarina.
            Depois, ordenou que, no jardim do palácio, fosse armada uma cortina branca contra a luz do sol, de modo que
            deixasse transparecer a luz. No dia da apresentação ao imperador e sua corte, o mago fez surgir, ao som de
            uma
            flauta, a sombra de uma bailarina movimentando-se com leveza e graciosidade. Neste momento, teria surgido o
            teatro de sombras</p>
